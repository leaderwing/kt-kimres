    /**
  * Convert FLAME string to UTF-8 string.
  * @param flameURL String FLAME string.
  * @return String The corresponding string in UTF-16.
  */
 public static String convert(String flameURL){
   String utfURL = new String(flameURL);
   for(int i=0; i<flameFlower2UTF.length; i++){
     StringTokenizer line = new StringTokenizer(new String(flameFlower2UTF[i]));
     String flame = line.nextToken();
     String utf = line.nextToken();
     String temp = utfURL.replaceAll(flame, utf);
     utfURL = new String(temp);
   }
   return utfURL;
 }
 /**
    * Convert an UTF-8 string to FLAME string.
    * @param utfURL String The UTF-8 string.
    * @return String The corresponding FLAME string.
    */
   public static String deconvert(String utfURL){
     String flameURL = new String(utfURL);
     for(int i=0; i<flameFlower2UTF.length; i++){
       StringTokenizer line = new StringTokenizer(new String(flameFlower2UTF[i]));
       String flame = line.nextToken();
       String utf = line.nextToken();
       String temp = flameURL.replaceAll(utf, flame);
       flameURL = new String(temp);
     }
     return flameURL;
  }
